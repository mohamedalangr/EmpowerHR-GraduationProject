const LanguageContext = createContext(null);

function interpolate(template, vars = {}) {
  return Object.entries(vars).reduce(
    (output, [key, value]) => output.replaceAll(`{{${key}}}`, value),
    template,
  );
}

export function LanguageProvider({ children }) {
  const [language, setLanguage] = useState(() => localStorage.getItem(STORAGE_KEY) || 'en');

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, language);
    const isArabic = language === 'ar';
    document.documentElement.lang = language;
    document.documentElement.dir = 'ltr';
    document.body.dir = 'ltr';
    document.documentElement.classList.toggle('app-rtl', isArabic);
    document.body.classList.toggle('app-rtl', isArabic);
  }, [language]);

  const toggleLanguage = useCallback(() => {
    setLanguage((prev) => (prev === 'en' ? 'ar' : 'en'));
  }, []);

  const t = useCallback((key, vars = {}) => {
    const table = translations[language] || translations.en;
    const text = table[key] || translations.en[key] || key;
    return interpolate(text, vars);
  }, [language]);

  const value = useMemo(() => ({
    language,
    setLanguage,
    toggleLanguage,
    isRTL: language === 'ar',
    t,
  }), [language, setLanguage, toggleLanguage, t]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) throw new Error('useLanguage must be used inside LanguageProvider');
  return context;
}
